package ClosedlabBook;

public class DefaultValuesOfPrimitiveTypes {
	public static void main(String[] args) {
		
		//
		byte b=0 ;
		short s = 0;
		int i=0;
		long l = 0L;
		float f = 0.0f;
		double d = 0.0d;
		char ch = '\u0000';
		String str = null;
		boolean bool = false;
		
		
		System.out.println("value of Byte is : "+ b);
		System.out.println("value of Short is : "+s);
		System.out.println("value of Int is : "+i);
		System.out.println("value of Long is:"+l);
		System.out.println("value of float is : "+f);
		System.out.println("value of Double is : "+d);
		System.out.println("value of Char is : "+ch);
		System.out.println("value of String is : "+str);
		System.out.println("value of Boolean is : "+bool);
		
	}

}
